
-- Car Event MAP Database Schema

CREATE TABLE areas (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  region TEXT,
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE spots (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  area_id BIGINT REFERENCES areas(id),
  lat DOUBLE PRECISION,
  lng DOUBLE PRECISION,
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE TABLE events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  spot_id BIGINT REFERENCES spots(id),
  event_date DATE,
  start_time TIME,
  description TEXT,
  source TEXT,
  created_at TIMESTAMP DEFAULT NOW()
);
